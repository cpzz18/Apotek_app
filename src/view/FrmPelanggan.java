    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed

     if (!validasiInput()) {
        return;
    }
    try {

        String sql = "INSERT INTO pelanggan "
                + "(nama_pelanggan, telepon, alamat, jenis_kelamin) "
                + "VALUES (?, ?, ?, ?)";

        Connection conn = Koneksi.getConnection();

        PreparedStatement pst =
                conn.prepareStatement(sql);

        pst.setString(1,
                txtNamaPelanggan.getText());

        pst.setString(2,
                txtTelepon.getText());

        pst.setString(3,
                txtAlamat.getText());

        pst.setString(4,
                cmbJenisKelamin.getSelectedItem().toString());

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this,
                "Data pelanggan berhasil disimpan");

        loadTable();
        resetForm();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(this,
                e.getMessage());
    }

    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here
        if(txtIdPelanggan.getText().trim().isEmpty()){
    JOptionPane.showMessageDialog(this,
            "Pilih data yang akan diedit!");
    return;
}
        if (!validasiInput()) {
    return;
}
        
        try {

        String sql = "UPDATE pelanggan "
                + "SET nama_pelanggan=?, "
                + "telepon=?, "
                + "alamat=?, "
                + "jenis_kelamin=? "
                + "WHERE id_pelanggan=?";

        Connection conn = Koneksi.getConnection();

        PreparedStatement pst =
                conn.prepareStatement(sql);

        pst.setString(1, txtNamaPelanggan.getText());
        pst.setString(2, txtTelepon.getText());
        pst.setString(3, txtAlamat.getText());
        pst.setString(4,
                cmbJenisKelamin.getSelectedItem().toString());

        pst.setInt(5,
                Integer.parseInt(txtIdPelanggan.getText()));

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this,
                "Data pelanggan berhasil diubah");

        loadTable();
        resetForm();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(this,
                e.getMessage());
    }
    }//GEN-LAST:event_btnEditActionPerformed

    private void txtHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtHapusActionPerformed
        // TODO add your handling code here:
        if(txtIdPelanggan.getText().trim().isEmpty()){
    JOptionPane.showMessageDialog(this,
            "Pilih data yang akan dihapus!");
    return;
}
        int konfirmasi =
        JOptionPane.showConfirmDialog(
                this,
                "Yakin ingin menghapus data?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION);

if(konfirmasi != JOptionPane.YES_OPTION){
    return;
}
        try {

        String sql =
                "DELETE FROM pelanggan WHERE id_pelanggan=?";

        Connection conn = Koneksi.getConnection();

        PreparedStatement pst =
                conn.prepareStatement(sql);

        pst.setInt(1,
                Integer.parseInt(txtIdPelanggan.getText()));

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this,
                "Data pelanggan berhasil dihapus");

        loadTable();
        resetForm();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(this,
                e.getMessage());
    }
    }//GEN-LAST:event_txtHapusActionPerformed

