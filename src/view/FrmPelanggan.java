private boolean validasiInput() {

    if (txtNamaPelanggan.getText().trim().isEmpty()) {

        JOptionPane.showMessageDialog(this,
                "Nama pelanggan wajib diisi!");

        txtNamaPelanggan.requestFocus();
        return false;
    }

    if (txtTelepon.getText().trim().isEmpty()) {

        JOptionPane.showMessageDialog(this,
                "Nomor telepon wajib diisi!");

        txtTelepon.requestFocus();
        return false;
    }

    return true;
}

private void loadJenisKelamin() {

    cmbJenisKelamin.removeAllItems();

    cmbJenisKelamin.addItem("Laki-Laki");
    cmbJenisKelamin.addItem("Perempuan");
}
private void loadTable() {

   DefaultTableModel model = new DefaultTableModel(
    new Object[][]{},
    new String[]{
        "ID",
        "Nama Pelanggan",
        "Telepon",
        "Alamat",
        "Jenis Kelamin"
    }
){
    @Override
    public boolean isCellEditable(int row, int column){
        return false;
    }
};

    try {

        String sql = "SELECT * FROM pelanggan";

        Connection conn = Koneksi.getConnection();

        Statement st = conn.createStatement();

        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {

            model.addRow(new Object[]{
                rs.getInt("id_pelanggan"),
                rs.getString("nama_pelanggan"),
                rs.getString("telepon"),
                rs.getString("alamat"),
                rs.getString("jenis_kelamin")
            });
        }

        tblPelanggan.setModel(model);
        tblPelanggan.setRowHeight(25);

    } catch (Exception e) {

        JOptionPane.showMessageDialog(this,
                "Gagal memuat data : " + e.getMessage());
    }
}
